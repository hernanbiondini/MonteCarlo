package integralmontecarlo;

/**
 *
 * @author hernan
 */
public class Respuesta {
    
    private String respuesta;
    private String estimacionError;
    private String respuestaConsola;
    private String detalle;

    public String getRespuesta() {
        return respuesta;
    }

    public void setRespuesta(String respuesta) {
        this.respuesta = respuesta;
    }

    public String getEstimacionError() {
        return estimacionError;
    }

    public void setEstimacionError(String estimacionError) {
        this.estimacionError = estimacionError;
    }

    public String getRespuestaConsola() {
        return respuestaConsola;
    }

    public void setRespuestaConsola(String respuestaConsola) {
        this.respuestaConsola = respuestaConsola;
    }

    public String getDetalle() {
        return detalle;
    }

    public void setDetalle(String detalle) {
        this.detalle = detalle;
    }
    
    
    
}
